    private void buttonEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonEntrarActionPerformed
        String msg = null;

        if (textLogin.getText().isEmpty() || textSenha.getPassword().length == 0) {
            msg = "Insira o usu√°rio e a senha";
        } else {
            JsonObject dados = new JsonObject();
            dados.addProperty("login", textLogin.getText());
            dados.addProperty("senha", new String(textSenha.getPassword()));
            msg = UsuarioController.autenticar(dados.toString());
        }

        if (msg == null) {
            this.setVisible(false);
            new Principal().setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, msg, "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_buttonEntrarActionPerformed

