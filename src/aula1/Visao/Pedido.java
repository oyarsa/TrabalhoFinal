    private void buttonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonExcluirActionPerformed
        desabilitarComponentes();

        int linha = tablePedidos.getSelectedRow();
        if (linha == -1) {
            return;
        }

        DefaultTableModel m = (DefaultTableModel) tablePedidos.getModel();

        String codigo = (String) m.getValueAt(linha, 0);
        PedidoController.excluir(codigo);

        m.removeRow(linha);
        limparComponentes();

        JOptionPane.showMessageDialog(this, "Operação executada com sucesso", "Sucesso",
                JOptionPane.PLAIN_MESSAGE);
    }//GEN-LAST:event_buttonExcluirActionPerformed

