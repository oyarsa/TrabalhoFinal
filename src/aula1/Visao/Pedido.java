    private void tablePedidosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablePedidosMouseClicked
        int linha = tablePedidos.getSelectedRow();
        if (linha == -1) {
            return;
        }

        DefaultTableModel m = (DefaultTableModel) tablePedidos.getModel();

        String codigo = m.getValueAt(linha, 0).toString();
        String json = PedidoController.recuperar(codigo);

        JsonObject dados = new Gson().fromJson(json, JsonObject.class);

        textCodigo.setText(dados.get("codigo").getAsString());
        textDescricao.setText(dados.get("descricao").getAsString());
        textValor.setText(dados.get("valorTotal").getAsString());
        textData.setText(dados.get("dataCriacao").getAsString());
        comboUsuario.setSelectedItem(codigoToUsuario.get(dados.get("codigoUsuario").getAsString()).getAsString());
    }//GEN-LAST:event_tablePedidosMouseClicked

