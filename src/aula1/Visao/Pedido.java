    private void btnRecalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRecalcularActionPerformed
        if (textCodigo.getText().equals("0")) {
            return;
        }

        String json = PedidoController.recalcularValorTotal(textCodigo.getText());
        JsonObject dados = new Gson().fromJson(json, JsonObject.class);

        if (dados.has("valorTotal")) {
            textValor.setText(dados.get("valorTotal").getAsString());
            salvar();
        }
    }//GEN-LAST:event_btnRecalcularActionPerformed

